#include "libft.h"

void		*ft_memcpy(void *dest, const void *src, size_t n)
{
	void	*ptr;
	size_t	i;

	i = 0;
	ptr = dest;
	if (!dest && !src)
		return (ptr);
	while (i < n)
	{
		*(char*)dest = *(char*)src;
		dest++;
		src++;
		i++;
	}
	return (ptr);
}
